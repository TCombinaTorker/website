<h1> Cryptography </h1>
<p> There are 2 traditional mathematical approach in cryptography.
The first and most essential approach is symetric cryptography.
And the second one is asymetric criptography.</p>

<h2> Encryption
</h2>
<p>An encryption algorithm is a mathematical function \(P, K \overset{E}{\longrightarrow} C \).
Where \(P \in {Alphabet}^* \simeq \mathbb{N} \) means plaintext, K means key and C means CIphertext.
(All P, K, and C can be represented with a natural number because the set of every word is isomorph to natural numbers)
</p>
<h2>Decryption
</h2>
<p> A decryption algorithm is reverse function D of an encryption function E.
    \(E^{-1} := C\) (Can be partial reverse if not reversable).
</p>


<h2> Symetric Cryptography
</h2>
<p>
    Let E be a an encryption function with two parameters.
    \[E(x,K) = E_K(x)\; bijective \iff E \;\;symetric \;\; encryption . \]
    For any key K.

</p>
<h2> Asymetric encryption
</h2>
<p> \[\lnot \;\; E \;\; symetric \Longrightarrow E \;\; asymetric. \]
</p>